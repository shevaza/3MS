var data;
var names = [];
var rates = [];
$.ajax({
    type: "POST",
    url: "../php_queries/ajax/maint_chart.php",
    data: {
        action: 'get'
    },
    dataType: "HTML",
    success: function(r) {
        data = JSON.parse(r);
        for (let i = 0; i < data.length; i++) {
            const element = data[i];
            names.push(element[0]);
            rates.push((element[2] / element[1]) * 100);
        }
        const maint_pchart = document.getElementById('Maintenance_PChart').getContext('2d');
        const mChart = new Chart(maint_pchart, {
            type: 'bar',
            data: {
                labels: names,
                datasets: [{
                    label: 'Task Success Rate',
                    data: rates,
                    backgroundColor: [
                        'rgba(255, 99, 132, 0.2)',
                        'rgba(54, 162, 235, 0.2)',
                        'rgba(255, 206, 86, 0.2)',
                        'rgba(75, 192, 192, 0.2)',
                        'rgba(153, 102, 255, 0.2)',
                        'rgba(255, 159, 64, 0.2)'
                    ],
                    borderColor: [
                        'rgba(255, 99, 132, 1)',
                        'rgba(54, 162, 235, 1)',
                        'rgba(255, 206, 86, 1)',
                        'rgba(75, 192, 192, 1)',
                        'rgba(153, 102, 255, 1)',
                        'rgba(255, 159, 64, 1)'
                    ],
                    borderWidth: 1
                }]
            },
            options: {
                scales: {
                    y: {
                        beginAtZero: true
                    }
                }
            }
        });




    }
});